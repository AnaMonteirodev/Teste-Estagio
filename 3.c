#include <stdio.h>

int main() {
    // a) 1, 3, 5, 7, ___
    // Lógica: Adicionar 2 ao número anterior
    printf("a) Próximo elemento: 9\n");

    // b) 2, 4, 8, 16, 32, 64, ____
    // Lógica: Multiplicar o número anterior por 2
    printf("b) Próximo elemento: 128\n");

    // c) 0, 1, 4, 9, 16, 25, 36, ____
    // Lógica: Quadrado dos números naturais
    printf("c) Próximo elemento: 49\n");

    // d) 4, 16, 36, 64, ____
    // Lógica: Quadrados dos números pares
    printf("d) Próximo elemento: 100\n");

    // e) 1, 1, 2, 3, 5, 8, ____
    // Lógica: Série de Fibonacci (soma dos dois números anteriores)
    printf("e) Próximo elemento: 13\n");

    // f) 2, 10, 12, 16, 17, 18, 19, ____
    // Lógica: Aproximação de números primos
    printf("f) Próximo elemento: 20\n");

    return 0;
}

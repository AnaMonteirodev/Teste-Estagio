#include <stdio.h>

int main() {
    // 1. Ligue o primeiro interruptor e espere alguns minutos.
    printf("1. Ligue o primeiro interruptor.\n");
    printf("   (Espere alguns minutos)\n");

    // 2. Desligue o primeiro interruptor e ligue o segundo interruptor.
    printf("2. Desligue o primeiro interruptor e ligue o segundo interruptor.\n");

    // 3. Entre na sala das lâmpadas.
    printf("3. Entre na sala das lâmpadas.\n");

    // 4. A lâmpada acesa estará conectada ao primeiro interruptor.
    printf("4. A lâmpada acesa está conectada ao primeiro interruptor.\n");

    // 5. Toque a lâmpada que estava apagada:
    printf("5. Toque a lâmpada que estava apagada:\n");
    printf("   - Se estiver quente, ela está conectada ao segundo interruptor.\n");
    printf("   - Se estiver fria, ela está conectada ao terceiro interruptor.\n");

    return 0;
}
